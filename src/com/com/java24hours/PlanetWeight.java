package com.java24hours;

public class PlanetWeight {
    public static void main(String[] args) {
        System.out.println("Your weight on Earth is:");
        double weight = 178;
        System.out.println(weight);
        
        System.out.println("Your weight on Mercury is:");
        double mercuryWeight = weight * .378;
        System.out.println(mercuryWeight);

        System.out.println("Your weight on the Moon is:");
        double moonWeight = weight * .166;
        System.out.println(moonWeight);

        System.out.println("Your weight on Jupitar is:");
        double jupitarWeight = weight * 2.364;
        System.out.println(jupitarWeight);

        System.out.println("Your weight on Venus is:");
        double venusWeight = weight * .907;
        System.out.println(venusWeight);

        System.out.println("Your weight on Uranus is:");
        double uranusWeight = weight * .889;
        System.out.println(uranusWeight);

        }
}
